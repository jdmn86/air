@LargeTest
@RunWith(AndroidJUnit4.class)
public class US6_AT5 {

    @Rule
    public ActivityTestRule<DashboardActivity> mActivityTestRule = new ActivityTestRule<>(DashboardActivity.class);

    private UiDevice mDevice;

    @Before
    public void setUp() {
        mDevice = UiDevice.getInstance(InstrumentationRegistry.getInstrumentation());
    }

    @Test
    public void dashboardActivityTest2() {

        openActionBarOverflowOrOptionsMenu(getInstrumentation().getTargetContext());

        ViewInteraction appCompatTextView = onView(
                allOf(withId(R.id.title), withText("Map"),
                        childAtPosition(
                                childAtPosition(
                                        withClassName(is("android.support.v7.view.menu.ListMenuItemView")),
                                        0),
                                0),
                        isDisplayed()));
        appCompatTextView.perform(click());
        UiObject map = mDevice.findObject(new UiSelector().description("Google Map"));
        try {
            map.getContentDescription();
        } catch (UiObjectNotFoundException e) {
            fail("Map not found");
        }

        UiObject searchBar = mDevice.findObject(new UiSelector().text("Search"));
        try {
            searchBar.getContentDescription();
        } catch (UiObjectNotFoundException e) {
            fail("Search bar not found");
        }
        UiObject searchImageButton = mDevice.findObject(new UiSelector().description("Search"));
        try {
            searchImageButton.getContentDescription();
        } catch (UiObjectNotFoundException e) {
            fail("Search bar image button not found");
        }
    }

    private static Matcher<View> childAtPosition(
            final Matcher<View> parentMatcher, final int position) {

        return new TypeSafeMatcher<View>() {
            @Override
            public void describeTo(Description description) {
                description.appendText("Child at position " + position + " in parent ");
                parentMatcher.describeTo(description);
            }

            @Override
            public boolean matchesSafely(View view) {
                ViewParent parent = view.getParent();
                return parent instanceof ViewGroup && parentMatcher.matches(parent)
                        && view.equals(((ViewGroup) parent).getChildAt(position));
            }
        };
    }
}
